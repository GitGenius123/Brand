import java.util.Scanner;
public class Loop2{
	public static void main(String args[]){
		int i=1;
		
		while(i<6){
			System.out.println(i+". Hello World");
			i++;
		}
		
		i=10;
		
		while(i>0){
			System.out.print(i+" ");
			i--;
		}
		
		System.out.println();
		
		i=2;
		while(i<22){
			System.out.print(i +" ");
			i+=2;
		}
		
	}
}